    private void findMyAccountBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findMyAccountBtnActionPerformed
        // TODO add your handling code here:
     String username = userNameTextField.getText();   
     account = system.getUserAccountDirectory().findUser(username);
     
     System.out.println(account);
        Enterprise inEnterprise = null;
            Organization inOrganization = null;
            if (account == null) {
                //Step2: Go inside each network to check each enterprise
                for (Network network : system.getNetworkList()) {
                    //Step 2-a: Check against each enterprise
                    // for (Country country : network.getCountryDirectory().getCountryList()) {
                    for (State state : network.getStateDirectory().getStateList()) {
                        for (City city : state.getCityDirectory().getCityList()) {
                            for (Enterprise enterprise : city.getEnterpriseDirectory().getEnterpriseList()) {
                                account = enterprise.getUserAccountDirectory().findUser(username);
                                if (account == null) {
                                    //Step3: Check against each organization inside that enterprise
                                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                                        account = organization.getUserAccountDirectory().findUser(username);
                                        if (account != null) {
                                            inEnterprise = enterprise;
                                            inOrganization = organization;
                                            break;
                                        }
                                    }
                                } else {
                                    inEnterprise = enterprise;
                                    break;
                                }
                                if (inOrganization != null) {
                                    break;
                                }
                            }
                        }
                    }
                    //}
                    if (inEnterprise != null) {
                        break;
                    }
                }
            }

            if (account == null) {
                JOptionPane.showMessageDialog(null, "Account Not Found. Create a new one now");
                return;
            } else {
    
                messageLabel.setText("");
                messageLabel.setText("We found your account, Please proceed to retrieve the password");
                userNameTextField.setEnabled(false);
               //JOptionPane.showMessageDialog(null, " We found your account, Please proceed to retrieve the password");
           }       
    }//GEN-LAST:event_findMyAccountBtnActionPerformed


    private void addCityButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCityButtonActionPerformed
        // TODO add your handling code here:
        if (!cityNameJTextField.getText().equals("")) {
            String name = cityNameJTextField.getText();

            City city = state.getCityDirectory().addCity();
            city.setName(name);

            populateCityTable();
            cityNameJTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please enter City name");
        }
    }//GEN-LAST:event_addCityButtonActionPerformed

    private void cityNameJTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cityNameJTextFieldKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if ((key >= KeyEvent.VK_A && key <= KeyEvent.VK_Z) || (key == KeyEvent.VK_CAPS_LOCK) || (key == KeyEvent.VK_BACK_SPACE) || (key == KeyEvent.VK_SHIFT) || (key == KeyEvent.VK_SPACE)) {
            cityNameJTextField.setEditable(true);
        } else {
            cityNameJTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, "Only alphabets are allowed");
            cityNameJTextField.setText("");
        }
    }//GEN-LAST:event_cityNameJTextFieldKeyPressed

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backButtonActionPerformed

    public void populateCityTable(){
        DefaultTableModel model = (DefaultTableModel) cityJTable.getModel();

        model.setRowCount(0);
        for (City city : state.getCityDirectory().getCityList()) {
            Object[] row = new Object[1];
            row[0] = city;
            model.addRow(row);
        }
    }
    
    
    


    private void enterpriseNameJTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_enterpriseNameJTextFieldKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if ((key >= KeyEvent.VK_A && key <= KeyEvent.VK_Z) || (key == KeyEvent.VK_CAPS_LOCK) || (key == KeyEvent.VK_BACK_SPACE) || (key == KeyEvent.VK_SHIFT) || (key == KeyEvent.VK_SPACE)) {
            enterpriseNameJTextField.setEditable(true);
        } else {
            enterpriseNameJTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, "Only alphabets are allowed");
            enterpriseNameJTextField.setText("");
        }
    }//GEN-LAST:event_enterpriseNameJTextFieldKeyPressed


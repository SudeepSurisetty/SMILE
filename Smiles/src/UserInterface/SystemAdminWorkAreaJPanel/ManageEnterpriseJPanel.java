    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        if (!enterpriseNameJTextField.getText().equals("")) {
            System.out.println("Inside submit button " + enterpriseNameJTextField.getText());
            Network network = (Network) networkComboBox.getSelectedItem();
            //Country country = (Country) countryComboBox.getSelectedItem();
            State state = (State) stateComboBox.getSelectedItem();
            City city = (City) cityComboBox.getSelectedItem();
            Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeComboBox.getSelectedItem();

            if (network == null || state == null || city == null || type == null) {
                JOptionPane.showMessageDialog(null, "Invalid Input!");
                return;
            }

            
            String name = enterpriseNameJTextField.getText();

            Enterprise enterprise = city.getEnterpriseDirectory().createAndAddEnterprise(name, type);

            populateTable();
            
            JOptionPane.showMessageDialog(null, "Enterprise created");
            enterpriseNameJTextField.setText("");
                    
        } else {
            JOptionPane.showMessageDialog(null, "Enter value", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnSubmitActionPerformed


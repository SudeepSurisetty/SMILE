    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        if(organizationNameTextField.getText().equals("") || organizationJComboBox.equals(null)){
            JOptionPane.showMessageDialog(null,"Please enter name and select organization type");
        }
        else{
        Type type = (Type) organizationJComboBox.getSelectedItem();
        Organization org = organizationDirectory.createOrganization(type);
        org.setName(organizationNameTextField.getText());
        
        JOptionPane.showMessageDialog(null,"Organization created");
        organizationNameTextField.setText("");
        
        populateTable();
        }
        
    }//GEN-LAST:event_addJButtonActionPerformed

    

    private void organizationNameTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_organizationNameTextFieldKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if ((key >= KeyEvent.VK_A && key <= KeyEvent.VK_Z) || (key == KeyEvent.VK_CAPS_LOCK) || (key == KeyEvent.VK_BACK_SPACE) || (key == KeyEvent.VK_SHIFT)) {
            organizationNameTextField.setEditable(true);
        } else {
            organizationNameTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, "Only alphabets are allowed");
            organizationNameTextField.setText("");
        }
    }//GEN-LAST:event_organizationNameTextFieldKeyPressed


